/** XP required to go from level N to level N+1 (1-indexed, length 199). */
export const MOUNT_XP_PER_LEVEL: readonly number[] = Array.from(
  { length: 199 },
  (_, i) => MOUNT_XP_CUMULATIVE[i + 1] - MOUNT_XP_CUMULATIVE[i],
);

/** Total XP needed to reach a given level (1–200). Returns 0 for level 1. */
export function xpToReachLevel(level: number): number {
  if (level < 1 || level > 200) return 0;
  return MOUNT_XP_CUMULATIVE[level - 1];
}

/** XP needed to go from `fromLevel` to `toLevel`. */
export function xpBetweenLevels(fromLevel: number, toLevel: number): number {
  return xpToReachLevel(toLevel) - xpToReachLevel(fromLevel);
}
